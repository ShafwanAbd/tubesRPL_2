     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $model = User::find($id);

        $model->name = $request->name;
        $model->email = $request->email;
        $model->alamat = $request->alamat;
        $model->nomerHP = $request->nomerHP;
        $model->save(); 

        if ($request->file('photo')){
            $file = $request->file('photo');
            // $namaFile = time().str_replace(" ", "", $model->name);
            $namaFile = "imgProfile_".$model->id.".png";
            $file->move('imgProfile', $namaFile);
            $model->photo = $namaFile;
            $model->save();
        }

        return redirect('profile')->with('success', 'Akun Berhasil Diperbaharui!');
    }

